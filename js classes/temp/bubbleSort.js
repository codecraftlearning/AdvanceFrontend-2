


function bubbleSort(arr) {
    const size = arr.length;
    
    console.log("Unsorted", arr);
    
    let count = 0;

    for(let i = 0; i<size-1; i++) {
        for (let j = i+1; j<size; j++) {
            if (arr[i] > arr[j]) {
                let temp = arr[i];
                arr[i] = arr[j];
                arr[j] = temp;
            }
            count++;
        }
    }

    console.log("sorted", arr);
    // console.log('count', count);
    
}


